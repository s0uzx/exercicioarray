
// Crio um array com saudações
const boasVindas = ['Olá', 'Seja', 'Bem-Vindo', '!'];

// Junto os elementos do array em uma string, separando-os por um espaço em branco
const mensagem = boasVindas.join(' ');

// Exibo a mensagem resultante no console
console.log(mensagem);

// Ficará assim no console:
// Olá Seja Bem-Vindo !

